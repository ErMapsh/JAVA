package  _5Loop;
public class WhileLoop {
    public static void main(String[] args) {
        /*---while Loops----

        int i = 0;
        while ( i < 5){
            System.out.println(i);
            i++;
        }

        */

        /* ----For loop----
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
        */


        /* ----Do while----*/

        int i = 5;
        do {
            System.out.println(i);
            i--;
        }while (i<1);


    }
}
