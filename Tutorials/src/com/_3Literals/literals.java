package com._3Literals;

public class literals{
    public static void main(String[] arg){
        /* float have can value double, so we need to use literals like 5.6f */

        System.out.println("----Literals----");
        byte a = 34;// 34 --> its begin automatically 34 as byte;
        int age = 18;// 18 --> integer literal
        long lng = 3444444444L;// 3444444444L --> long literal
        char ch = 'A';// 'A' --> character literals
        float f1 = 5.6f; // 5.6 --> is by default double, but we have float,so we can use literal like this 5.6f, default type for decimals
        double d1 = 4.555d;// in advance, we can give literals like this
        boolean bl = true;// true --> boolean literal
        String str = "Hello World"; // "Hello World" --> String Literal

        System.out.println(str);


    }
}
