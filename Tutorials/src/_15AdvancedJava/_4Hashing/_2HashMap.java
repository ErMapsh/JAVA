package _15AdvancedJava._4Hashing;

import java.util.HashMap;

public class _2HashMap {
    public static void main(String[] args) {
        HashMap<String, String> hm = new HashMap<>();

        hm.put("The book", "13/10/22 10:52 AM");
        hm.put("Human", "13/10/22 10:53 AM");
        System.out.println(hm);
    }
}
